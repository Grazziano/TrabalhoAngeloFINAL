    private void cadastrar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrar

        Cliente c = new Cliente();
        c.setBairro(this.tfBairro.getText());
        c.setCidade(this.tfCidade.getText());
        c.setCpf(this.tfCPF.getText());
        c.setNome(this.tfNome.getText());
        c.setRua(this.tfRua.getText());

        try {
            /////CADASTRA INFORMA√áOES NA BASE DE DADOS
            new ClienteValid().inserir(c);
            /////////////MSG DE SUCESSO
            JOptionPane.showMessageDialog(this, "Cadastro Realizado com Sucesso", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
            limparFormulario();
        } catch (RuntimeException r) {
            JOptionPane.showMessageDialog(this, r.getMessage(), "ATEN√á√ÉO!!!", JOptionPane.WARNING_MESSAGE);

        }

       

    }//GEN-LAST:event_cadastrar

    private void limparFormulario() {
        this.tfBairro.setText("");
        this.tfCidade.setText("");
        this.tfCPF.setText("");
        this.tfNome.setText("");
        this.tfRua.setText("");
    }

