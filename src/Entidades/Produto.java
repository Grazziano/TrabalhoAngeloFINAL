public class Produto extends Cliente {

    private String quantidade;
    private String fornecedor;
    private String validade;
    private String valor_venda;
    private String Valor_custo;
    private String NCM;
    private String CFOP;
    private String CSOSN;
    private String CST;
    private String ICM;
    private String MVA;

    /**
     * @return the id
     */
    public String getQuantidade() {
        return quantidade;
    }

    /**
     * @param quantidade the quantidade to set
     */
    public void setQuantidade(String quantidade) {
        this.quantidade = quantidade;
    }

    /**
     * @return the fornecedor
     */
    public String getFornecedor() {
        return fornecedor;
    }

    /**
     * @param fornecedor the fornecedor to set
     */
    public void setFornecedor(String fornecedor) {
        this.fornecedor = fornecedor;
    }

    /**
     * @return the validade
     */
    public String getValidade() {
        return validade;
    }

    /**
     * @param validade the validade to set
     */
    public void setValidade(String validade) {
        this.validade = validade;
    }

    /**
     * @return the valor_venda
     */
    public String getValor_venda() {
        return valor_venda;
    }

    /**
     * @param valor_venda the valor_venda to set
     */
    public void setValor_venda(String valor_venda) {
        this.valor_venda = valor_venda;
    }
    
   
    

    /**
     * @return the Valor_custo
     */
    public String getValor_custo() {
        return Valor_custo;
    }

    /**
     * @param Valor_custo the Valor_custo to set
     */
    public void setValor_custo(String Valor_custo) {
        this.Valor_custo = Valor_custo;
    }

    /**
     * @return the NCM
     */
    public String getNCM() {
        return NCM;
    }

    /**
     * @param NCM the NCM to set
     */
    public void setNCM(String NCM) {
        this.NCM = NCM;
    }

    /**
     * @return the CFOP
     */
    public String getCFOP() {
        return CFOP;
    }

    /**
     * @param CFOP the CFOP to set
     */
    public void setCFOP(String CFOP) {
        this.CFOP = CFOP;
    }

    /**
     * @return the CSOSN
     */
    public String getCSOSN() {
        return CSOSN;
    }

    /**
     * @param CSOSN the CSOSN to set
     */
    public void setCSOSN(String CSOSN) {
        this.CSOSN = CSOSN;
    }

    /**
     * @return the CST
     */
    public String getCST() {
        return CST;
    }

    /**
     * @param CST the CST to set
     */
    public void setCST(String CST) {
        this.CST = CST;
    }

    /**
     * @return the ICM
     */
    public String getICM() {
        return ICM;
    }

    /**
     * @param ICM the ICM to set
     */
    public void setICM(String ICM) {
        this.ICM = ICM;
    }

    /**
     * @return the MVA
     */
    public String getMVA() {
        return MVA;
    }

    /**
     * @param MVA the MVA to set
     */
    public void setMVA(String MVA) {
        this.MVA = MVA;
    }

    /**
     * @return the id
     */
}
